/* Arithmetic Operator */
/*

  + -> addition
  - -> subtraction
  * -> multiplication
  / -> division
  % -> reminder(modulo)
 ~/ -> division return integer

 */
int main(){
  int? x = 10;
  int? y = 3;
  print(x + y); //13
  print(x - y); //7
  print(x * y); //30
  print(x / y); //3.33333333
  print(x % y); //1
  print(x ~/ y); //3

  return 0;
}
